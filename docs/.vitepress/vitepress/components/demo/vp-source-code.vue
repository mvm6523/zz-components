<script setup lang="ts">
import { computed } from 'vue'

const props = defineProps({
	source: {
		type: String,
		required: true,
	},
})

const decoded = computed(() => {
	return decodeURIComponent(<string>props.source)
})
</script>

<template>
	<div class="example-source-wrapper">
		<div class="example-source language-vue" v-html="decoded" />
	</div>
</template>

<style scoped>
.example-source {
	background-color: var(--z-code-background) !important;
}

.language-vue {
	margin: 0 !important;
	border-radius: 0 !important;
}
</style>
